use serde::{Deserialize, Serialize};
use std::fs;
use std::path::PathBuf;

#[derive(Debug, Serialize, Deserialize, Clone, Default)]
pub struct Config {
    pub kalshi_api_key_id: Option<String>,
    pub kalshi_private_key: Option<String>,
    pub webhook_url: Option<String>,
    #[serde(default)]
    pub kalshi_is_demo: bool,
    #[serde(default)]
    pub rapidapi_key: Option<String>,
    #[serde(default)]
    pub perplexity_api_key: Option<String>,
    #[serde(default)]
    pub ai_agent_mode: bool,
    /// Selected market categories (e.g. ["sports:nba", "crypto:all", "politics:us_elections"])
    /// Empty or ["all"] means watch everything
    #[serde(default = "default_categories")]
    pub categories: Vec<String>,
    /// Default whale alert threshold in USD
    #[serde(default = "default_threshold")]
    pub threshold: u64,
    /// Bet size in USD for automated execution
    #[serde(default = "default_bet_size")]
    pub bet_size: f64,
    pub discord_webhook_url: Option<String>,
    /// Which platforms to monitor: ["polymarket", "kalshi"] or ["all"]
    #[serde(default = "default_platforms")]
    pub platforms: Vec<String>,
    /// Days to retain alerts in the database (0 = keep forever)
    #[serde(default = "default_retention_days")]
    pub history_retention_days: u32,
    /// Maximum odds to alert on (0.0-1.0). Skip if YES or NO price exceeds this.
    /// Default 0.95 filters out near-certainties with no edge.
    #[serde(default = "default_max_odds")]
    pub max_odds: f64,
    /// Minimum spread to alert on. Skip dead/settled markets with 0 spread.
    /// Default 0.0 (disabled).
    #[serde(default = "default_min_spread")]
    pub min_spread: f64,
    /// Ollama model for Polymarketâ†’Kalshi market matching (e.g. "llama3", "qwen2.5-coder:7b").
    #[serde(default = "default_ollama_model")]
    pub ollama_model: String,
    /// Ollama embedding model for first-stage retrieval (e.g. "nomic-embed-text").
    #[serde(default = "default_ollama_embed_model")]
    pub ollama_embed_model: String,
    /// Ollama API base URL for generate endpoint.
    #[serde(default = "default_ollama_url")]
    pub ollama_url: String,
    /// Maximum entry price in cents for Kalshi orders (default 97).
    /// Trades above this are skipped to avoid fees eating profits.
    #[serde(default = "default_max_entry_price_cents")]
    pub max_entry_price_cents: i64,
    /// Maximum fraction of bankroll to risk per trade (default 0.02 = 2%).
    #[serde(default = "default_max_bet_fraction")]
    pub max_bet_fraction: f64,
    /// Hard cap on any single bet in dollars (default $10).
    #[serde(default = "default_max_bet_cap")]
    pub max_bet_cap: f64,
    /// Maximum simultaneous open positions (default 5).
    #[serde(default = "default_max_open_positions")]
    pub max_open_positions: usize,
    /// Daily stop-loss as a fraction of day-start balance (default 0.10 = 10%).
    #[serde(default = "default_daily_loss_limit")]
    pub daily_loss_limit: f64,
    /// Minimum bankroll reserve as a fraction of day-start balance (default 0.20 = 20%).
    #[serde(default = "default_reserve_fraction")]
    pub reserve_fraction: f64,
}

